package com.example.multiplegranarymanager.A_WuWeiHuaiNanPackage.Body;

public class InsectList {
    private String name;
    private int insect;

    public InsectList(String name, int insect) {
        this.name = name;
        this.insect = insect;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getInsect() {
        return insect;
    }

    public void setInsect(int insect) {
        this.insect = insect;
    }
}
