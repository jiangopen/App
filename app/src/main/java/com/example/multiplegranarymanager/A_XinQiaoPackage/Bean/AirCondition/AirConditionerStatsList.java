package com.example.multiplegranarymanager.A_XinQiaoPackage.Bean.AirCondition;

public class AirConditionerStatsList {
    private Double humidity;
    private String inspurAddress;
    private String status;
    private Double temp;

    public Double getHumidity() {
        return humidity;
    }

    public void setHumidity(Double humidity) {
        this.humidity = humidity;
    }

    public String getInspurAddress() {
        return inspurAddress;
    }

    public void setInspurAddress(String inspurAddress) {
        this.inspurAddress = inspurAddress;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Double getTemp() {
        return temp;
    }

    public void setTemp(Double temp) {
        this.temp = temp;
    }
}
